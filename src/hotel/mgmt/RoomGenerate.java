    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String a1=jTextField2.getText();
        try{               
             if(jTextField1==null)
             {
            JOptionPane.showMessageDialog(this, " Enter Room ID No for save ");
             }
          else   if(a1.matches("[0-9]*"))
             {
        String sql;
             sql = "insert into room(Ro_No,Type,Size,Others,Price) values (?,?,?,?,?)";
        pst=conn.prepareStatement(sql);
        pst.setString(1, jTextField1.getText());
        pst.setString(2, (String) jComboBox1.getSelectedItem());
        pst.setString(3, (String) jComboBox2.getSelectedItem());
        pst.setString(4, (String) jComboBox3.getSelectedItem());
        pst.setString(5, (String) jTextField2.getText());  
    
        pst.execute();
            JOptionPane.showMessageDialog(null , "Saved");
           
             }
             else
             {
            JOptionPane.showMessageDialog(this, " Invalid Value ");
             }
        }
        catch(SQLException | HeadlessException e)
        {
            JOptionPane.showMessageDialog(null , e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

