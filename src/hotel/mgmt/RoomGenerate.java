    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try{                
          String sql="select * from room order by Ro_No DESC limit 1";
          pst=conn.prepareStatement(sql);
          rs=pst.executeQuery();
          if(rs.next())
          {
          String no=rs.getString("Ro_No");
          int id=Integer.parseInt(no);
          id=id+1;
          String ids=Integer.toString(id);
          jTextField1.setText(ids);             
          }
                }
                catch(SQLException | NumberFormatException ex)
                {
            JOptionPane.showMessageDialog(null , ex);
                                  }   

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTextField2MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextField2MouseEntered
        int pric,rt=0,rs=0,no=0;
        
        if(jComboBox1.getSelectedItem()==" 7   Star")
        {
            rt=2000;
        }else if(jComboBox1.getSelectedItem()==" 5   Star")
        {
            rt=1500;
        }else if(jComboBox1.getSelectedItem()==" 3   Star")
        {
            rt=1000;
        }else
        {
            rt=750;
        }
        if(jComboBox2.getSelectedItem()==" Single")
        {
            rs=0;
        }else if(jComboBox2.getSelectedItem()==" Double")
        {
            rs=250;
        }else
        {
            rs=500;
        }
        if(jComboBox3.getSelectedItem()==" 3")
        {
            no=100;
        }else if(jComboBox3.getSelectedItem()==" 4")
        {
            no=250;
        }else
        {
            no=0;
        }
        pric=rs+rt+no;
        String show=Integer.toString(pric);
        jTextField2.setText(show);
    }//GEN-LAST:event_jTextField2MouseEntered

    
